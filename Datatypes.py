#integer data type
a = 10
print(a)
#float data type
b = 3.14
print(b)
#string data type
c = "String"
print(c)
#boolean data type
d = True #it can also be false
print(d)
#set data type which is a collection of key-value pairs
e = {1, 2, 3}
print(e)

#type() is function is used to check the data type of the variable 
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))