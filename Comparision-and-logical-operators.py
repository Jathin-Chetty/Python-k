#comparision operators
a = 10
b = 5
print(a == b) # result will be true if both variables are equal else false
print(a != b) #result will be false if variables are not equal else true
print(a > b) #result will be true if left variable is greater than right variable else false
print(a < b) #result will be true if left variable is less than right variable else false
print(a <= b) #result will be true if left variable is less than or equal to right variable else false
print(a >= b) #result will be true if left variable is greater than or equal to right variable else false