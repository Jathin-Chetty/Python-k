#Logical operators
a = 10
b = 5
#Logical and operator-the result of the 'and' statement will be true if both statements will evaluate to true else it will result to false
print((a > b) and (a < b))
#Logical or operator-the result of the or statement will be true even if one of the statement evalute to true and other statement will evaluate to false
print((a > b) or (a < b))
#Logical not operator-the result of the program will be opposite of the result we obtain i.e if we get the value of a > b as true and the output value will be false and alse vice-versa
print(not(a > b))
#in the above statement the original output is true but since we are applying not operator we get the opposite of the true i.e false